package cn.enaium.cafemod.utility

import imgui.ImGui
import java.util.concurrent.ConcurrentHashMap
import java.util.concurrent.atomic.AtomicInteger
import kotlin.properties.ReadWriteProperty
import kotlin.reflect.KProperty

class ImState<T>(var value: T) : ReadWriteProperty<Any?, T> {
    override fun getValue(thisRef: Any?, property: KProperty<*>): T = value
    override fun setValue(thisRef: Any?, property: KProperty<*>, value: T) {
        this.value = value
    }
}

object ImRemember {
    private val stateMap = ConcurrentHashMap<String, ImState<*>>()

    private val windowIndices = ConcurrentHashMap<String, AtomicInteger>()
    private val usedKeysThisFrame = ConcurrentHashMap.newKeySet<String>()

    fun beginState() {
        usedKeysThisFrame.clear()
        windowIndices.values.forEach { it.set(0) }
    }

    fun endState() {
        val iterator = stateMap.entries.iterator()
        while (iterator.hasNext()) {
            val key = iterator.next().key
            if (!usedKeysThisFrame.contains(key)) {
                iterator.remove()
            }
        }
    }

    @Suppress("UNCHECKED_CAST")
    fun <T> remember(
        explicitKey: String? = null,
        init: () -> T
    ): ImState<T> {
        val windowName = ImGui.getWindowViewport().id.toString()

        val indexCounter = windowIndices.getOrPut(windowName) { AtomicInteger(0) }

        val actualKey = if (explicitKey != null) {
            "$windowName:k_$explicitKey"
        } else {
            "$windowName:i_${indexCounter.getAndIncrement()}"
        }

        usedKeysThisFrame.add(actualKey)

        val state = stateMap.getOrPut(actualKey) {
            ImState(init())
        }

        val currentValue = state.value
        val newValue = init()

        if (newValue != null) {
            if (currentValue != null && currentValue::class != newValue::class) {
                val newState = ImState(newValue)
                stateMap[actualKey] = newState
                return newState as ImState<T>
            }
        }

        return state as ImState<T>
    }
}